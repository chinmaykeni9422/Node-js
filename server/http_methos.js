// HTTP method
// get, post, put, patch, delete

// GET -> when you want to get some data from server

// POST -> when you want to send or mutate(change or update) some data into server

// PUT -> The HTTP PUT request method is used to create a new resource or overwrite a representation of the target resource that is known by the client

// PATCH -> The PATCH method applies  modifications to a data.

// DELETE -> The DELETE method deletes the specified data.